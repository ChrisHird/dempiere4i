-- Table: i_payment

-- DROP TABLE i_payment;

CREATE TABLE i_payment
(
  i_payment_id                   FOR COLUMN i_payid         DECIMAL(10,0) NOT NULL,
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0),
  ad_org_id                                                 DECIMAL(10,0),
  isactive                                                  CHAR(1) DEFAULT 'Y',
  created                                                   TIMESTAMP WITH DEFAULT CURRENT_TIMESTAMP,
  createdby                                                 DECIMAL(10,0),
  updated                                                   TIMESTAMP WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0),
  i_isimported                   FOR COLUMN i_ii            CHAR(1) NOT NULL DEFAULT 'N',
  i_errormsg                                                VARCHAR(2000),
  processed                                                 CHAR(1) DEFAULT 'N',
  c_payment_id                   FOR COLUMN c_payid         DECIMAL(10,0),
  documentno                                                VARCHAR(30),
  datetrx                                                   TIMESTAMP,
  isreceipt                                                 CHAR(1) DEFAULT 'Y',
  c_doctype_id                   FOR COLUMN c_dtid          DECIMAL(10,0),
  doctypename                    FOR COLUMN doctypnam       VARCHAR(60),
  trxtype                                                   CHAR(1),
  c_bankaccount_id               FOR COLUMN c_baid          DECIMAL(10,0),
  bankaccountno                  FOR COLUMN bactnum         VARCHAR(20),
  c_bpartner_id                  FOR COLUMN c_bpid          DECIMAL(10,0),
  bpartnervalue                  FOR COLUMN bpvalue         VARCHAR(40),
  c_invoice_id                   FOR COLUMN c_iid           DECIMAL(10,0),
  invoicedocumentno              FOR COLUMN invdocnum       VARCHAR(30),
  tendertype                                                CHAR(1),
  creditcardtype                 FOR COLUMN cctyp           CHAR(1),
  creditcardnumber               FOR COLUMN ccnum           VARCHAR(20),
  creditcardvv                   FOR COLUMN ccvv            VARCHAR(4),
  creditcardexpmm                FOR COLUMN ccemm           DECIMAL(10,0),
  creditcardexpyy                FOR COLUMN cceyy           DECIMAL(10,0),
  micr                                                      VARCHAR(20),
  routingno                                                 VARCHAR(20),
  accountno                                                 VARCHAR(20),
  checkno                                                   VARCHAR(20),
  a_name                                                    VARCHAR(60),
  a_street                                                  VARCHAR(60),
  a_city                                                    VARCHAR(60),
  a_state                                                   VARCHAR(40),
  a_zip                                                     VARCHAR(20),
  a_country                                                 VARCHAR(40),
  a_ident_dl                                                VARCHAR(20),
  a_ident_ssn                    FOR COLUMN a_issn          VARCHAR(20),
  a_email                                                   VARCHAR(60),
  voiceauthcode                  FOR COLUMN vautcde         VARCHAR(20),
  swipe                                                     VARCHAR(80),
  orig_trxid                                                VARCHAR(20),
  ponum                                                     VARCHAR(60),
  c_currency_id                  FOR COLUMN c_cuid          DECIMAL(10,0),
  payamt                                                    DECFLOAT(16) DEFAULT 0,
  discountamt                    FOR COLUMN dscamt          DECFLOAT(16) DEFAULT 0,
  writeoffamt                    FOR COLUMN woamt           DECFLOAT(16) DEFAULT 0,
  isoverunderpayment             FOR COLUMN isoupmt         CHAR(1) DEFAULT 'N',
  overunderamt                   FOR COLUMN ouamt           DECFLOAT(16) DEFAULT 0,
  c_charge_id                    FOR COLUMN c_cgid          DECIMAL(10,0),
  chargename                                                VARCHAR(60),
  chargeamt                                                 DECFLOAT(16) DEFAULT 0,
  taxamt                                                    DECFLOAT(16) DEFAULT 0,
  isapproved                                                CHAR(1) DEFAULT 'N',
  isselfservice                  FOR COLUMN isslfsrv        CHAR(1) DEFAULT 'N',
  isdelayedcapture               FOR COLUMN isdldcap        CHAR(1) DEFAULT 'N',
  r_pnref                                                   VARCHAR(20),
  r_result                                                  VARCHAR(20),
  r_respmsg                                                 VARCHAR(60),
  r_authcode                                                VARCHAR(20),
  r_info                                                    VARCHAR(2000),
  processing                                                CHAR(1),
  iso_code                                                  character(3),
  dateacct                                                  TIMESTAMP,
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  CONSTRAINT i_payment_pkey PRIMARY KEY (i_payment_id),
  CONSTRAINT i_payment_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT i_payment_isapproved_check CHECK (isapproved in ('Y','N')),
  CONSTRAINT i_payment_isdelayedcapture_check CHECK (isdelayedcapture in ('Y','N')),
  CONSTRAINT i_payment_isoverunderpayment_check CHECK (isoverunderpayment in ('Y','N')),
  CONSTRAINT i_payment_isreceipt_check CHECK (isreceipt in ('Y','N')),
  CONSTRAINT i_payment_isselfservice_check CHECK (isselfservice in ('Y','N')),
  CONSTRAINT i_payment_processed_check CHECK (processed in ('Y','N'))
)
;

