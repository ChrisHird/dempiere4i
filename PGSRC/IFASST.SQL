CREATE TABLE i_fixedasset FOR SYSTEM NAME ifasst
(
  i_fixedasset_id                FOR COLUMN i_faid          DECIMAL(10,0) NOT NULL,
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0) NOT NULL,
  ad_org_id                                                 DECIMAL(10,0) NOT NULL,
  isactive                                                  CHAR(1) NOT NULL DEFAULT 'Y',
  created                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  createdby                                                 DECIMAL(10,0) NOT NULL,
  updated                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0) NOT NULL,
  inventoryno                    FOR COLUMN ivtnum          VARCHAR(30) DEFAULT NULL,
  name                                                      VARCHAR(120) DEFAULT NULL,
  a_asset_group_id               FOR COLUMN a_agid          DECIMAL(10,0),
  a_asset_group_value            FOR COLUMN a_agrpval       VARCHAR(40) DEFAULT NULL,
  a_asset_class_id               FOR COLUMN a_aclsid        DECIMAL(10,0),
  a_asset_class_value            FOR COLUMN a_aclsval       VARCHAR(40) DEFAULT NULL,
  m_product_id                   FOR COLUMN m_pid           DECIMAL(10,0),
  productvalue                   FOR COLUMN prodval         VARCHAR(40) DEFAULT NULL,
  assetservicedate               FOR COLUMN asd             TIMESTAMP,
  a_asset_cost                   FOR COLUMN a_acst          DECFLOAT(16) NOT NULL DEFAULT 0,
  a_accumulated_depr             FOR COLUMN a_adep          DECFLOAT(16),
  a_accumulated_depr_f           FOR COLUMN a_adepf         DECFLOAT(16),
  uselifemonths                  FOR COLUMN ulm             DECIMAL(10,0),
  uselifemonths_f                FOR COLUMN uselfmf         DECIMAL(10,0),
  a_current_period               FOR COLUMN a_curpd         DECIMAL(10,0),
  dateacct                                                  TIMESTAMP,
  a_asset_id                                                DECIMAL(10,0),
  i_isimported                   FOR COLUMN i_ii            CHAR(1) NOT NULL DEFAULT 'N',
  i_errormsg                                                VARCHAR(2000) DEFAULT NULL,
  processing                                                CHAR(1) DEFAULT NULL,
  processed                                                 CHAR(1) DEFAULT NULL,
  a_remaining_period             FOR COLUMN a_rmnpd         DECIMAL(10,0) NOT NULL DEFAULT 0,
  docaction                                                 VARCHAR(2) NOT NULL DEFAULT 'CO',
  assetperioddepreciationamt     FOR COLUMN apddepamt       DECFLOAT(16) NOT NULL DEFAULT 0,
  assetdepreciationdate          FOR COLUMN addte           TIMESTAMP,
  m_locator_id                   FOR COLUMN m_lid           DECIMAL(10,0),
  locatorvalue                   FOR COLUMN locval          VARCHAR(40) DEFAULT NULL,
  qty                                                       DECFLOAT(16) DEFAULT 1,
  uomsymbol                                                 VARCHAR(10) DEFAULT NULL,
  c_uom_id                                                  DECIMAL(10,0),
  description                    FOR COLUMN desc            VARCHAR(255) DEFAULT NULL,
  c_city_value                   FOR COLUMN c_ctyval        VARCHAR(40) DEFAULT NULL,
  seqno                                                     DECIMAL(10,0) DEFAULT 1,
  bpartner_value                 FOR COLUMN bpval           VARCHAR(40) DEFAULT NULL,
  c_bpartnersr_id                FOR COLUMN c_bpsrid        DECIMAL(10,0),
  c_city_id                                                 DECIMAL(10,0),
  a_asset_type_id                FOR COLUMN a_atypid        DECIMAL(10,0),
  a_asset_type_value             FOR COLUMN a_atypval       VARCHAR(40) DEFAULT NULL,
  a_salvage_value                FOR COLUMN a_svgval        DECFLOAT(16),
  orgvalue                                                  VARCHAR(40) DEFAULT NULL,
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  CONSTRAINT i_fixedasset_key PRIMARY KEY (i_fixedasset_id),
  CONSTRAINT i_fixedasset_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT i_fixedasset_processed_check CHECK (processed in ('Y','N'))
)
;

