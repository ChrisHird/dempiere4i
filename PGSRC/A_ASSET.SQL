(
  a_asset_id                                                DECIMAL(10,0) NOT NULL,
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0) NOT NULL,
  ad_org_id                                                 DECIMAL(10,0) NOT NULL,
  isactive                                                  CHAR(1) NOT NULL DEFAULT 'Y',
  created                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  createdby                                                 DECIMAL(10,0) NOT NULL,
  updated                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0) NOT NULL,
  value                                                     VARCHAR(40) NOT NULL,
  name                                                      VARCHAR(60) NOT NULL,
  description                    FOR COLUMN desc            VARCHAR(255),
  help                                                      VARCHAR(2000),
  a_asset_group_id               FOR COLUMN a_agid          DECIMAL(10,0) NOT NULL,
  m_product_id                   FOR COLUMN m_pid           DECIMAL(10,0) NOT NULL,
  serno                                                     VARCHAR(255),
  lot                                                       VARCHAR(255),
  versionno                                                 VARCHAR(20),
  guaranteedate                  FOR COLUMN gdate           TIMESTAMP,
  assetservicedate               FOR COLUMN asd             TIMESTAMP,
  isowned                                                   CHAR(1) NOT NULL DEFAULT 'Y',
  assetdepreciationdate          FOR COLUMN addte           TIMESTAMP,
  uselifeyears                   FOR COLUMN uly             DECIMAL(10,0),
  uselifemonths                  FOR COLUMN ulm             DECIMAL(10,0),
  lifeuseunits                   FOR COLUMN luu             DECFLOAT(16),
  useunits                                                  DECFLOAT(16),
  isdisposed                                                CHAR(1) NOT NULL DEFAULT 'N',
  assetdisposaldate              FOR COLUMN add             TIMESTAMP,
  isinposession                  FOR COLUMN isinposn        CHAR(1) NOT NULL DEFAULT 'Y',
  locationcomment                FOR COLUMN loccmt          VARCHAR(255),
  m_locator_id                   FOR COLUMN m_lid           DECIMAL(10,0),
  c_bpartner_id                  FOR COLUMN c_bpid          DECIMAL(10,0),
  c_bpartner_location_id         FOR COLUMN c_bplid         DECIMAL(10,0),
  c_location_id                  FOR COLUMN c_lid           DECIMAL(10,0),
  processing                                                CHAR(1) DEFAULT 'N',
  isdepreciated                  FOR COLUMN isdeptd         CHAR(1) NOT NULL DEFAULT 'Y',
  isfullydepreciated             FOR COLUMN isflydep        CHAR(1) NOT NULL DEFAULT 'N',
  ad_user_id                                                DECIMAL(10,0),
  m_attributesetinstance_id      FOR COLUMN m_asi           DECIMAL(10,0) DEFAULT 0,
  qty                                                       DECFLOAT(16),
  c_project_id                   FOR COLUMN c_prjid         DECIMAL(10,0),
  c_bpartnersr_id                FOR COLUMN c_bpsrid        DECIMAL(10,0),
  m_inoutline_id                 FOR COLUMN m_iolid         DECIMAL(10,0),
  lastmaintenencedate            FOR COLUMN lstmntdte       TIMESTAMP,
  nextmaintenencedate            FOR COLUMN nmde            TIMESTAMP,
  lastmaintenanceuseunit         FOR COLUMN lmuu            DECIMAL(10,0),
  nextmaintenanceuseunit         FOR COLUMN nmuu            DECIMAL(10,0),
  leaseterminationdate           FOR COLUMN ltd             TIMESTAMP,
  lease_bpartner_id              FOR COLUMN lbpid           DECIMAL(10,0),
  lastmaintenancenote            FOR COLUMN lstmntnte       VARCHAR(60),
  lastmaintenancedate            FOR COLUMN lmd             TIMESTAMP,
  lastmaintenanceunit            FOR COLUMN lstmntunit      DECIMAL(10,0),
  nextmaintenenceunit            FOR COLUMN nxtmntu         DECIMAL(10,0),
  a_asset_createdate             FOR COLUMN a_acrtdte       TIMESTAMP,
  a_asset_revaldate              FOR COLUMN a_arvdte        TIMESTAMP,
  a_parent_asset_id              FOR COLUMN a_paid          DECIMAL(10,0),
  a_qty_current                  FOR COLUMN a_qcur          DECFLOAT(16),
  a_qty_original                 FOR COLUMN a_qorig         DECFLOAT(16),
  a_asset_class_id               FOR COLUMN a_aclsid        DECIMAL(10,0),
  a_asset_status                 FOR COLUMN a_asts          VARCHAR(2) NOT NULL DEFAULT 'NW',
  a_asset_action                 FOR COLUMN a_asact         VARCHAR(2) NOT NULL DEFAULT 'MD',
  processed                                                 CHAR(1) NOT NULL DEFAULT 'N',
  assetactivationdate            FOR COLUMN aadate          TIMESTAMP,
  inventoryno                    FOR COLUMN ivtnum          VARCHAR(30) DEFAULT NULL,
  a_asset_type_id                FOR COLUMN a_atypid        DECIMAL(10,0),
  a_assettype                    FOR COLUMN a_atype         VARCHAR(3) NOT NULL DEFAULT 'MFX',
  c_activity_id                  FOR COLUMN c_aid           DECIMAL(10,0),
  manufacturer                   FOR COLUMN manuf           VARCHAR(30) DEFAULT NULL,
  manufacturedyear               FOR COLUMN manufyr         DECIMAL(10,0),
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  a_asset_split_id               FOR COLUMN a_aspid         DECIMAL(10,0),
  CONSTRAINT a_asset_pkey PRIMARY KEY (a_asset_id),
  CONSTRAINT a_asset_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT a_asset_isdisposed_check CHECK (isdisposed in ('Y','N')),
  CONSTRAINT a_asset_isinposession_check CHECK (isinposession in ('Y','N')),
  CONSTRAINT a_asset_isowned_check CHECK (isowned in ('Y','N')),
  CONSTRAINT a_asset_processed_check CHECK (processed in ('Y','N'))
)
;

