  processing                                                CHAR(1) DEFAULT 'N',
  ref_order_id                   FOR COLUMN refordid        DECIMAL(10,0),
  salesrep_id                    FOR COLUMN slsrepid        DECIMAL(10,0),
  sendemail                                                 CHAR(1) NOT NULL,
