  trackingno                                                VARCHAR(60),
  updated                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0) NOT NULL,
  user1_id                                                  DECIMAL(10,0),
  user2_id                                                  DECIMAL(10,0),
  volume                                                    DECFLOAT(16),
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0) NOT NULL,
  weight                                                    DECFLOAT(16),
  processedon                    FOR COLUMN prcon           DECFLOAT(16),
  m_freightcategory_id           FOR COLUMN m_fcid          DECIMAL(10,0),
  user3_id                                                  DECIMAL(10,0),
  user4_id                                                  DECIMAL(10,0),
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  CONSTRAINT dd_order_pkey PRIMARY KEY (dd_order_id),
  CONSTRAINT dd_order_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT dd_order_isapproved_check CHECK (isapproved in ('Y','N')),
  CONSTRAINT dd_order_isdelivered_check CHECK (isdelivered in ('Y','N')),
  CONSTRAINT dd_order_isdropship_check CHECK (isdropship in ('Y','N')),
  CONSTRAINT dd_order_isindispute_check CHECK (isindispute in ('Y','N')),
  CONSTRAINT dd_order_isintransit_check CHECK (isintransit in ('Y','N')),
  CONSTRAINT dd_order_isprinted_check CHECK (isprinted in ('Y','N')),
  CONSTRAINT dd_order_isselected_check CHECK (isselected in ('Y','N')),
  CONSTRAINT dd_order_issotrx_check CHECK (issotrx in ('Y','N')),
  CONSTRAINT dd_order_processed_check CHECK (processed in ('Y','N')),
  CONSTRAINT dd_order_sendemail_check CHECK (sendemail in ('Y','N'))
)
