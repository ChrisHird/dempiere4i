  totalinvqty                    FOR COLUMN totinvqty       DECFLOAT(16),
  orgvalue                                                  VARCHAR(40) DEFAULT NULL,
  activityvalue                  FOR COLUMN atvyval         VARCHAR(40) DEFAULT NULL,
  bpartnervalue                  FOR COLUMN bpvalue         VARCHAR(40) DEFAULT NULL,
  campaignvalue                  FOR COLUMN campval         VARCHAR(40) DEFAULT NULL,
