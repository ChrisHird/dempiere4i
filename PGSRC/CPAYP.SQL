-- Table: c_paymentprocessor

-- DROP TABLE c_paymentprocessor;

CREATE TABLE c_paymentprocessor FOR SYSTEM NAME cpayp
(
  c_paymentprocessor_id          FOR COLUMN c_ppid          DECIMAL(10,0) NOT NULL,
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0) NOT NULL,
  ad_org_id                                                 DECIMAL(10,0) NOT NULL,
  isactive                                                  CHAR(1) NOT NULL DEFAULT 'Y',
  created                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  createdby                                                 DECIMAL(10,0) NOT NULL,
  updated                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0) NOT NULL,
  name                                                      VARCHAR(60) NOT NULL,
  description                    FOR COLUMN desc            VARCHAR(255),
  c_bankaccount_id               FOR COLUMN c_baid          DECIMAL(10,0) NOT NULL,
  ad_sequence_id                 FOR COLUMN ad_seqid        DECIMAL(10,0),
  payprocessorclass              FOR COLUMN payprccls       VARCHAR(60),
  userid                                                    VARCHAR(60),
  password                                                  VARCHAR(60),
  hostaddress                    FOR COLUMN hostaddr        VARCHAR(60),
  hostport                                                  DECIMAL(10,0),
  proxyaddress                   FOR COLUMN pxyaddr         VARCHAR(60),
  proxyport                                                 DECIMAL(10,0),
  proxylogon                                                VARCHAR(60),
  proxypassword                  FOR COLUMN pxypwd          VARCHAR(60),
  acceptvisa                                                CHAR(1) NOT NULL DEFAULT 'Y',
  acceptmc                                                  CHAR(1) NOT NULL DEFAULT 'Y',
  acceptamex                                                CHAR(1) NOT NULL DEFAULT 'Y',
  acceptdiners                   FOR COLUMN actdin          CHAR(1) NOT NULL DEFAULT 'Y',
  acceptdiscover                 FOR COLUMN acpdscv         CHAR(1) NOT NULL DEFAULT 'Y',
  acceptcorporate                FOR COLUMN acpcorp         CHAR(1) NOT NULL DEFAULT 'Y',
  acceptcheck                    FOR COLUMN acpchk          CHAR(1) NOT NULL DEFAULT 'Y',
  acceptatm                                                 CHAR(1) NOT NULL DEFAULT 'Y',
  requirevv                                                 CHAR(1) NOT NULL DEFAULT 'N',
  c_currency_id                  FOR COLUMN c_cuid          DECIMAL(10,0),
  costpertrx                                                DECFLOAT(16) NOT NULL DEFAULT 0,
  commission                                                DECFLOAT(16) NOT NULL,
  partnerid                                                 VARCHAR(60),
  vendorid                                                  VARCHAR(60),
  minimumamt                                                DECFLOAT(16) DEFAULT 0,
  acceptdirectdebit              FOR COLUMN acpddb          CHAR(1) NOT NULL DEFAULT 'N',
  acceptdirectdeposit            FOR COLUMN acpddp          CHAR(1) NOT NULL DEFAULT 'N',
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  CONSTRAINT c_paymentprocessor_pkey PRIMARY KEY (c_paymentprocessor_id),
  CONSTRAINT c_paymentprocessor_acceptamex_check CHECK (acceptamex in ('Y','N')),
  CONSTRAINT c_paymentprocessor_acceptatm_check CHECK (acceptatm in ('Y','N')),
  CONSTRAINT c_paymentprocessor_acceptcheck_check CHECK (acceptcheck in ('Y','N')),
  CONSTRAINT c_paymentprocessor_acceptcorporate_check CHECK (acceptcorporate in ('Y','N')),
  CONSTRAINT c_paymentprocessor_acceptdiners_check CHECK (acceptdiners in ('Y','N')),
  CONSTRAINT c_paymentprocessor_acceptdiscover_check CHECK (acceptdiscover in ('Y','N')),
  CONSTRAINT c_paymentprocessor_acceptmc_check CHECK (acceptmc in ('Y','N')),
  CONSTRAINT c_paymentprocessor_acceptvisa_check CHECK (acceptvisa in ('Y','N')),
  CONSTRAINT c_paymentprocessor_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT c_paymentprocessor_requirevv_check CHECK (requirevv in ('Y','N'))
)
;

