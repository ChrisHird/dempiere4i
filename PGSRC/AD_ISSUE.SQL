-- Table: ad_issue

-- DROP TABLE ad_issue;

CREATE TABLE ad_issue
(
  ad_issue_id                    FOR COLUMN ad_issid        DECIMAL(10,0) NOT NULL,
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0) NOT NULL,
  ad_org_id                                                 DECIMAL(10,0) NOT NULL,
  isactive                                                  CHAR(1) NOT NULL DEFAULT 'Y',
  created                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  createdby                                                 DECIMAL(10,0) NOT NULL,
  updated                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0) NOT NULL,
  releaseno                                                 VARCHAR(10) NOT NULL DEFAULT '.',
  version                                                   VARCHAR(40) NOT NULL,
  name                                                      VARCHAR(60) NOT NULL,
  username                                                  VARCHAR(60) NOT NULL,
  supportemail                   FOR COLUMN spptem          VARCHAR(60),
  dbaddress                                                 VARCHAR(255),
  local_host                                                VARCHAR(120),
  operatingsysteminfo            FOR COLUMN osinfo          VARCHAR(255),
  releasetag                                                VARCHAR(60),
  databaseinfo                   FOR COLUMN dbinfo          VARCHAR(255),
  javainfo                                                  VARCHAR(255),
  remote_addr                    FOR COLUMN rmtaddr         VARCHAR(60),
  remote_host                    FOR COLUMN rmthost         VARCHAR(120),
  issuesummary                   FOR COLUMN issuesum        VARCHAR(2000) NOT NULL,
  comments                                                  VARCHAR(2000),
  sourceclassname                FOR COLUMN srcclsnm        VARCHAR(60),
  sourcemethodname               FOR COLUMN srcmthdnm       VARCHAR(60),
  loggername                                                VARCHAR(60),
  lineno                                                    DECIMAL(10,0) DEFAULT 0,
  stacktrace                                                VARCHAR(2000),
  errortrace                                                VARCHAR(2000),
  record_id                                                 DECIMAL(10,0),
  requestdocumentno              FOR COLUMN reqdocno        VARCHAR(30),
  a_asset_id                                                DECIMAL(10,0),
  r_request_id                   FOR COLUMN r_rid           DECIMAL(10,0),
  responsetext                   FOR COLUMN resptxt         VARCHAR(2000),
  processing                                                CHAR(1),
  processed                                                 CHAR(1) NOT NULL DEFAULT 'N',
  isvanillasystem                FOR COLUMN isvnlasys       CHAR(1) DEFAULT 'N',
  isreproducible                 FOR COLUMN isreprod        CHAR(1) DEFAULT 'N',
  r_issueknown_id                FOR COLUMN r_issknid       DECIMAL(10,0),
  statisticsinfo                 FOR COLUMN statsinf        VARCHAR(255),
  profileinfo                    FOR COLUMN prfinfo         VARCHAR(255),
  systemstatus                   FOR COLUMN sysstatus       CHAR(1) NOT NULL,
  r_issueproject_id              FOR COLUMN r_issprjid      DECIMAL(10,0),
  r_issueuser_id                 FOR COLUMN r_issusrid      DECIMAL(10,0),
  r_issuesystem_id               FOR COLUMN r_isssysid      DECIMAL(10,0),
  issuesource                    FOR COLUMN issuesrc        CHAR(1),
  ad_window_id                   FOR COLUMN ad_winid        DECIMAL(10,0),
  ad_process_id                  FOR COLUMN ad_pid          DECIMAL(10,0),
  ad_form_id                                                DECIMAL(10,0),
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  CONSTRAINT ad_issue_pkey PRIMARY KEY (ad_issue_id),
  CONSTRAINT ad_issue_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT ad_issue_isreproducible_check CHECK (isreproducible in ('Y','N')),
  CONSTRAINT ad_issue_isvanillasystem_check CHECK (isvanillasystem in ('Y','N')),
  CONSTRAINT ad_issue_processed_check CHECK (processed in ('Y','N'))
)
;

