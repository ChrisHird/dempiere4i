CREATE TABLE w_store
(
  w_store_id                                                DECIMAL(10,0) NOT NULL,
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0) NOT NULL,
  ad_org_id                                                 DECIMAL(10,0) NOT NULL,
  isactive                                                  CHAR(1) NOT NULL DEFAULT 'Y',
  created                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  createdby                                                 DECIMAL(10,0) NOT NULL,
  updated                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0) NOT NULL,
  name                                                      VARCHAR(60) NOT NULL,
  description                    FOR COLUMN desc            VARCHAR(255),
  help                                                      VARCHAR(2000),
  wstoreemail                    FOR COLUMN wstoreem        VARCHAR(60),
  wstoreuser                                                VARCHAR(60),
  wstoreuserpw                   FOR COLUMN wstoreupw       VARCHAR(20),
  webinfo                                                   VARCHAR(2000),
  webparam1                                                 VARCHAR(2000),
  webparam2                                                 VARCHAR(2000),
  webparam3                                                 VARCHAR(2000),
  webparam4                                                 VARCHAR(2000),
  webparam5                                                 VARCHAR(2000),
  webparam6                                                 VARCHAR(2000),
  ismenuassets                   FOR COLUMN ismnuast        CHAR(1) NOT NULL DEFAULT 'Y',
  ismenuorders                   FOR COLUMN ismnuord        CHAR(1) NOT NULL DEFAULT 'Y',
  ismenuinvoices                 FOR COLUMN ismnuinv        CHAR(1) NOT NULL DEFAULT 'Y',
  ismenushipments                FOR COLUMN ismnuship       CHAR(1) NOT NULL DEFAULT 'Y',
  ismenupayments                 FOR COLUMN ismnupmt        CHAR(1) NOT NULL DEFAULT 'Y',
  ismenurfqs                                                CHAR(1) NOT NULL DEFAULT 'Y',
  ismenurequests                 FOR COLUMN ismnureq        CHAR(1) NOT NULL DEFAULT 'Y',
  ismenuinterests                FOR COLUMN ismnuintr       CHAR(1) NOT NULL DEFAULT 'Y',
  ismenuregistrations            FOR COLUMN ismnureg        CHAR(1) NOT NULL DEFAULT 'Y',
  ismenucontact                  FOR COLUMN ismnucnt        CHAR(1) NOT NULL DEFAULT 'Y',
  emailheader                    FOR COLUMN emhdr           VARCHAR(2000),
  emailfooter                    FOR COLUMN emftr           VARCHAR(2000),
  salesrep_id                    FOR COLUMN slsrepid        DECIMAL(10,0) NOT NULL,
  m_warehouse_id                 FOR COLUMN m_wid           DECIMAL(10,0) NOT NULL,
  m_pricelist_id                 FOR COLUMN m_plid          DECIMAL(10,0) NOT NULL,
  webcontext                                                VARCHAR(20) NOT NULL,
  weborderemail                  FOR COLUMN woem            VARCHAR(60),
  c_paymentterm_id               FOR COLUMN c_ptid          DECIMAL(10,0),
  isdefault                                                 CHAR(1) NOT NULL DEFAULT 'N',
  url                                                       VARCHAR(120) NOT NULL DEFAULT 'http://localhost',
  stylesheet                                                VARCHAR(60),
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  CONSTRAINT w_store_pkey PRIMARY KEY (w_store_id),
  CONSTRAINT w_store_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT w_store_ismenuassets_check CHECK (ismenuassets in ('Y','N')),
  CONSTRAINT w_store_ismenucontact_check CHECK (ismenucontact in ('Y','N')),
  CONSTRAINT w_store_ismenuinterests_check CHECK (ismenuinterests in ('Y','N')),
  CONSTRAINT w_store_ismenuinvoices_check CHECK (ismenuinvoices in ('Y','N')),
  CONSTRAINT w_store_ismenuorders_check CHECK (ismenuorders in ('Y','N')),
  CONSTRAINT w_store_ismenupayments_check CHECK (ismenupayments in ('Y','N')),
  CONSTRAINT w_store_ismenuregistrations_check CHECK (ismenuregistrations in ('Y','N')),
  CONSTRAINT w_store_ismenurequests_check CHECK (ismenurequests in ('Y','N')),
  CONSTRAINT w_store_ismenurfqs_check CHECK (ismenurfqs in ('Y','N')),
  CONSTRAINT w_store_ismenushipments_check CHECK (ismenushipments in ('Y','N'))
)
;

