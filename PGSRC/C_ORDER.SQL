  dateacct                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  c_bpartner_id                  FOR COLUMN c_bpid          DECIMAL(10,0) NOT NULL,
  c_bpartner_location_id         FOR COLUMN c_bplid         DECIMAL(10,0) NOT NULL,
  poreference                    FOR COLUMN poref           VARCHAR(20),
  isdiscountprinted              FOR COLUMN isdscprt        CHAR(1) NOT NULL DEFAULT 'Y',
  c_currency_id                  FOR COLUMN c_cuid          DECIMAL(10,0) NOT NULL,
  paymentrule                    FOR COLUMN payrule         CHAR(1) NOT NULL,
