  CONSTRAINT c_order_isselected_check CHECK (isselected in ('Y','N')),
  CONSTRAINT c_order_issotrx_check CHECK (issotrx in ('Y','N')),
  CONSTRAINT c_order_istaxincluded_check CHECK (istaxincluded in ('Y','N')),
  CONSTRAINT c_order_istransferred_check CHECK (istransferred in ('Y','N')),
