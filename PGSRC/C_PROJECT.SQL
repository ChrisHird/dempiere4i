  salesrep_id                    FOR COLUMN slsrepid        DECIMAL(10,0),
  copyfrom                                                  CHAR(1),
  c_projecttype_id               FOR COLUMN c_prjtid        DECIMAL(10,0),
  committedqty                   FOR COLUMN cmtqty          DECFLOAT(16) NOT NULL DEFAULT 0,
  invoicedamt                    FOR COLUMN invamt          DECFLOAT(16) NOT NULL DEFAULT 0,
  invoicedqty                    FOR COLUMN invqty          DECFLOAT(16) NOT NULL DEFAULT 0,
  projectbalanceamt              FOR COLUMN pbamt           DECFLOAT(16) NOT NULL DEFAULT 0,
  c_phase_id                                                DECIMAL(10,0),
  iscommitceiling                FOR COLUMN iscmtceil       CHAR(1) NOT NULL DEFAULT 'N',
