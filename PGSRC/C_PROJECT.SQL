  CONSTRAINT c_project_generateto_check CHECK (generateto in ('Y','N')),
  CONSTRAINT c_project_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT c_project_iscommitment_check CHECK (iscommitment in ('Y','N')),
  CONSTRAINT c_project_isindefinite_check CHECK (isindefinite in ('Y','N')),
  CONSTRAINT c_project_processed_check CHECK (processed in ('Y','N'))
