CREATE TABLE i_bankstatement FOR SYSTEM NAME ibs
(
  i_bankstatement_id             FOR COLUMN i_bsid          DECIMAL(10,0) NOT NULL,
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0),
  ad_org_id                                                 DECIMAL(10,0),
  isactive                                                  CHAR(1) DEFAULT 'Y',
  created                                                   TIMESTAMP WITH DEFAULT CURRENT_TIMESTAMP,
  createdby                                                 DECIMAL(10,0),
  updated                                                   TIMESTAMP WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0),
  i_isimported                   FOR COLUMN i_ii            CHAR(1) NOT NULL DEFAULT 'N',
  i_errormsg                                                VARCHAR(2000),
  processed                                                 CHAR(1) DEFAULT 'N',
  c_bankstatement_id             FOR COLUMN c_bsid          DECIMAL(10,0),
  statementdate                  FOR COLUMN stmtdat         TIMESTAMP,
  description                    FOR COLUMN desc            VARCHAR(255),
  c_bankaccount_id               FOR COLUMN c_baid          DECIMAL(10,0),
  routingno                                                 VARCHAR(20),
  bankaccountno                  FOR COLUMN bactnum         VARCHAR(20),
  c_payment_id                   FOR COLUMN c_payid         DECIMAL(10,0),
  paymentdocumentno              FOR COLUMN paydocnum       VARCHAR(30),
  c_currency_id                  FOR COLUMN c_cuid          DECIMAL(10,0),
  iso_code                                                  character(3),
  c_bpartner_id                  FOR COLUMN c_bpid          DECIMAL(10,0),
  name                                                      VARCHAR(60),
  bpartnervalue                  FOR COLUMN bpvalue         VARCHAR(40),
  c_invoice_id                   FOR COLUMN c_iid           DECIMAL(10,0),
  invoicedocumentno              FOR COLUMN invdocnum       VARCHAR(30),
  c_charge_id                    FOR COLUMN c_cgid          DECIMAL(10,0),
  chargename                                                VARCHAR(60),
  chargeamt                                                 DECFLOAT(16) DEFAULT 0,
  c_bankstatementline_id         FOR COLUMN c_bslnid        DECIMAL(10,0),
  line                                                      DECIMAL(10,0),
  dateacct                                                  TIMESTAMP,
  valutadate                                                TIMESTAMP,
  statementlinedate              FOR COLUMN stmlndat        TIMESTAMP,
  trxtype                                                   VARCHAR(20),
  referenceno                    FOR COLUMN refnum          VARCHAR(40),
  memo                                                      VARCHAR(255),
  isreversal                                                CHAR(1) DEFAULT 'N',
  interestamt                    FOR COLUMN intamt          DECFLOAT(16) DEFAULT 0,
  trxamt                                                    DECFLOAT(16) DEFAULT 0,
  linedescription                FOR COLUMN lindesc         VARCHAR(255),
  stmtamt                                                   DECFLOAT(16) DEFAULT 0,
  eftstatementdate               FOR COLUMN eftstdat        TIMESTAMP,
  eftstatementreference          FOR COLUMN eftstref        VARCHAR(60),
  eftstatementlinedate           FOR COLUMN eftsld          TIMESTAMP,
  eftvalutadate                  FOR COLUMN eftvdat         TIMESTAMP,
  eftreference                   FOR COLUMN eftref          VARCHAR(60),
  eftcheckno                                                VARCHAR(20),
  efttrxid                                                  VARCHAR(40),
  efttrxtype                                                VARCHAR(20),
  eftmemo                                                   VARCHAR(2000),
  eftpayee                                                  VARCHAR(255),
  eftpayeeaccount                FOR COLUMN eftpact         VARCHAR(40),
  eftamt                                                    DECFLOAT(16) DEFAULT 0,
  eftcurrency                    FOR COLUMN eftcur          VARCHAR(20),
  createpayment                  FOR COLUMN crtpmt          CHAR(1),
  processing                                                CHAR(1),
  matchstatement                 FOR COLUMN mchstm          CHAR(1),
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  CONSTRAINT i_bankstatement_pkey PRIMARY KEY (i_bankstatement_id),
  CONSTRAINT i_bankstatement_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT i_bankstatement_isreversal_check CHECK (isreversal in ('Y','N')),
  CONSTRAINT i_bankstatement_processed_check CHECK (processed in ('Y','N'))
)
;

