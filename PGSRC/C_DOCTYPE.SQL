CREATE TABLE c_doctype
(
  c_doctype_id                   FOR COLUMN c_dtid          DECIMAL(10,0) NOT NULL,
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0) NOT NULL,
  ad_org_id                                                 DECIMAL(10,0) NOT NULL,
  isactive                                                  CHAR(1) NOT NULL DEFAULT 'Y',
  created                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  createdby                                                 DECIMAL(10,0) NOT NULL,
  updated                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0) NOT NULL,
  name                                                      VARCHAR(60) NOT NULL,
  printname                                                 VARCHAR(60) NOT NULL,
  description                    FOR COLUMN desc            VARCHAR(255),
  docbasetype                    FOR COLUMN docbsetyp       character(3) NOT NULL,
  issotrx                                                   CHAR(1) NOT NULL DEFAULT 'Y',
  docsubtypeso                   FOR COLUMN docsubtso       CHAR(2),
  hasproforma                    FOR COLUMN hasprfma        CHAR(1) DEFAULT 'N',
  c_doctypeproforma_id           FOR COLUMN c_dtpfid        DECIMAL(10,0),
  c_doctypeshipment_id           FOR COLUMN c_dtsid         DECIMAL(10,0),
  c_doctypeinvoice_id            FOR COLUMN c_dtiid         DECIMAL(10,0),
  isdocnocontrolled              FOR COLUMN isdocnctl       CHAR(1) NOT NULL DEFAULT 'N',
  docnosequence_id               FOR COLUMN docnseqid       DECIMAL(10,0),
  gl_category_id                 FOR COLUMN gl_cid          DECIMAL(10,0) NOT NULL,
  hascharges                                                CHAR(1) NOT NULL DEFAULT 'N',
  documentnote                   FOR COLUMN docnote         VARCHAR(2000),
  isdefault                                                 CHAR(1) NOT NULL DEFAULT 'N',
  documentcopies                 FOR COLUMN doccp           DECIMAL(10,0) NOT NULL,
  ad_printformat_id              FOR COLUMN ad_pfmtid       DECIMAL(10,0),
  isdefaultcounterdoc            FOR COLUMN isdftcdoc       CHAR(1) NOT NULL DEFAULT 'N',
  isshipconfirm                  FOR COLUMN isshpcfm        CHAR(1) NOT NULL DEFAULT 'N',
  ispickqaconfirm                FOR COLUMN ispqacfm        CHAR(1) NOT NULL DEFAULT 'N',
  isintransit                    FOR COLUMN isintrans       CHAR(1) NOT NULL DEFAULT 'N',
  issplitwhendifference          FOR COLUMN ispltwdif       CHAR(1) NOT NULL DEFAULT 'N',
  c_doctypedifference_id         FOR COLUMN c_dtdid         DECIMAL(10,0),
  iscreatecounter                FOR COLUMN iscrtcntr       CHAR(1) NOT NULL DEFAULT 'Y',
  isindexed                                                 CHAR(1) NOT NULL DEFAULT 'Y',
  isoverwriteseqoncomplete       FOR COLUMN isoseqcmp       CHAR(1) DEFAULT 'N',
  definitesequence_id            FOR COLUMN dfeseqid        DECIMAL(10,0),
  isoverwritedateoncomplete      FOR COLUMN isodtecmp       CHAR(1) DEFAULT 'N',
  ispreparesplitdocument         FOR COLUMN isprepsdoc      CHAR(1) NOT NULL DEFAULT 'Y',
  printdocument                  FOR COLUMN prtdoc          CHAR(1) DEFAULT NULL,
  iscopydocnoonreversal          FOR COLUMN iscpydnrv       CHAR(1) DEFAULT 'N',
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  isreversedwithoriginalacct     FOR COLUMN isrvwoact       CHAR(1) DEFAULT 'N',
  isallocateimmediate            FOR COLUMN isalloci        CHAR(1) DEFAULT 'N',
  c_doctypepayment_id            FOR COLUMN c_dtpmtid       DECIMAL(10,0),
  isbanktransfer                 FOR COLUMN isbnktr         CHAR(1) DEFAULT 'N',
  CONSTRAINT c_doctype_pkey PRIMARY KEY (c_doctype_id),
  CONSTRAINT c_doctype_hascharges_check CHECK (hascharges in ('Y','N')),
  CONSTRAINT c_doctype_hasproforma_check CHECK (hasproforma in ('Y','N')),
  CONSTRAINT c_doctype_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT c_doctype_isallocateimmediate_check CHECK (isallocateimmediate in ('Y','N')),
  CONSTRAINT c_doctype_isbanktransfer_check CHECK (isbanktransfer in ('Y','N')),
  CONSTRAINT c_doctype_iscopydocnoonreversal_check CHECK (iscopydocnoonreversal in ('Y','N')),
  CONSTRAINT c_doctype_isdocnocontrolled_check CHECK (isdocnocontrolled in ('Y','N')),
  CONSTRAINT c_doctype_isoverwritedateoncomplete_check CHECK (isoverwritedateoncomplete in ('Y','N')),
  CONSTRAINT c_doctype_isoverwriteseqoncomplete_check CHECK (isoverwriteseqoncomplete in ('Y','N')),
  CONSTRAINT c_doctype_ispreparesplitdocument_check CHECK (ispreparesplitdocument in ('Y','N')),
  CONSTRAINT c_doctype_isreversedwithoriginalacct_check CHECK (isreversedwithoriginalacct in ('Y','N')),
  CONSTRAINT c_doctype_issotrx_check CHECK (issotrx in ('Y','N'))
)
;

