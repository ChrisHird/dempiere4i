  dateacct                                                   TIMESTAMP NOT NULL WITH DEFAULT CURRENT_TIMESTAMP,
  c_bpartner_id                  FOR COLUMN c_bpid          DECIMAL(10,0) NOT NULL,
  c_bpartner_location_id         FOR COLUMN c_bplid         DECIMAL(10,0) NOT NULL,
  poreference                    FOR COLUMN poref           VARCHAR(20),
  isdiscountprinted              FOR COLUMN isdscprt        CHAR(1) NOT NULL DEFAULT 'Y',
  dateordered                    FOR COLUMN datord          TIMESTAMP,
  c_currency_id                  FOR COLUMN c_cuid          DECIMAL(10,0) NOT NULL,
  paymentrule                    FOR COLUMN payrule         CHAR(1) NOT NULL,
  c_paymentterm_id               FOR COLUMN c_ptid          DECIMAL(10,0) NOT NULL,
  c_charge_id                    FOR COLUMN c_cgid          DECIMAL(10,0),
  chargeamt                                                 DECFLOAT(16) DEFAULT 0,
  totallines                                                DECFLOAT(16) NOT NULL DEFAULT 0,
  grandtotal                                                DECFLOAT(16) NOT NULL DEFAULT 0,
  m_pricelist_id                 FOR COLUMN m_plid          DECIMAL(10,0) NOT NULL,
  istaxincluded                  FOR COLUMN itinc           CHAR(1) NOT NULL DEFAULT 'N',
  c_campaign_id                  FOR COLUMN c_camid         DECIMAL(10,0),
  c_project_id                   FOR COLUMN c_prjid         DECIMAL(10,0),
  c_activity_id                  FOR COLUMN c_aid           DECIMAL(10,0),
  ispaid                                                    CHAR(1) NOT NULL DEFAULT 'N',
  c_payment_id                   FOR COLUMN c_payid         DECIMAL(10,0),
  c_cashline_id                  FOR COLUMN c_clid          DECIMAL(10,0),
  createfrom                                                CHAR(1),
  generateto                                                CHAR(1),
  sendemail                                                 CHAR(1) NOT NULL DEFAULT 'N',
  ad_user_id                                                DECIMAL(10,0),
  copyfrom                                                  CHAR(1),
  isselfservice                  FOR COLUMN isslfsrv        CHAR(1) NOT NULL DEFAULT 'N',
  ad_orgtrx_id                   FOR COLUMN ad_otid         DECIMAL(10,0),
  user1_id                                                  DECIMAL(10,0),
  user2_id                                                  DECIMAL(10,0),
  c_conversiontype_id            FOR COLUMN c_cnvtid        DECIMAL(10,0),
  ispayschedulevalid             FOR COLUMN ispscdval       CHAR(1) NOT NULL DEFAULT 'N',
  ref_invoice_id                 FOR COLUMN refinvid        DECIMAL(10,0),
  isindispute                    FOR COLUMN isdispute       CHAR(1) NOT NULL DEFAULT 'N',
  invoicecollectiontype          FOR COLUMN invcoltyp       CHAR(1),
  m_rma_id                                                  DECIMAL(10,0),
  dunninggrace                   FOR COLUMN dungrace        TIMESTAMP,
  c_dunninglevel_id              FOR COLUMN c_dlid          DECIMAL(10,0),
  reversal_id                    FOR COLUMN revid           DECIMAL(10,0),
  processedon                    FOR COLUMN prcon           DECFLOAT(16),
  isfixedassetinvoice            FOR COLUMN isfxdasti       CHAR(1),
  c_pos_id                                                  DECIMAL(10,0),
  user3_id                                                  DECIMAL(10,0),
  user4_id                                                  DECIMAL(10,0),
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  CONSTRAINT c_invoice_pkey PRIMARY KEY (c_invoice_id),
  CONSTRAINT c_invoice_createfrom_check CHECK (createfrom in ('Y','N')),
  CONSTRAINT c_invoice_generateto_check CHECK (generateto in ('Y','N')),
  CONSTRAINT c_invoice_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT c_invoice_isapproved_check CHECK (isapproved in ('Y','N')),
  CONSTRAINT c_invoice_isdiscountprinted_check CHECK (isdiscountprinted in ('Y','N')),
  CONSTRAINT c_invoice_isfixedassetinvoice_check CHECK (isfixedassetinvoice in ('Y','N')),
  CONSTRAINT c_invoice_ispaid_check CHECK (ispaid in ('Y','N')),
  CONSTRAINT c_invoice_isprinted_check CHECK (isprinted in ('Y','N')),
  CONSTRAINT c_invoice_issotrx_check CHECK (issotrx in ('Y','N')),
  CONSTRAINT c_invoice_istaxincluded_check CHECK (istaxincluded in ('Y','N')),
  CONSTRAINT c_invoice_istransferred_check CHECK (istransferred in ('Y','N')),
  CONSTRAINT c_invoice_processed_check CHECK (processed in ('Y','N'))
)
;

