  volume                                                    DECFLOAT(16) DEFAULT 0,
  weight                                                    DECFLOAT(16) DEFAULT 0,
  shelfwidth                                                DECIMAL(10,0),
  shelfheight                    FOR COLUMN slfhgt          DECFLOAT(16),
  shelfdepth                                                DECIMAL(10,0),
  unitsperpallet                 FOR COLUMN uppal           DECFLOAT(16),
  c_taxcategory_id               FOR COLUMN c_tcid          DECIMAL(10,0) NOT NULL,
  s_resource_id                  FOR COLUMN s_rid           DECIMAL(10,0),
  discontinued                   FOR COLUMN discont         CHAR(1) DEFAULT 'N',
  discontinuedby                 FOR COLUMN dscby           TIMESTAMP,
  processing                                                CHAR(1),
  s_expensetype_id               FOR COLUMN s_etid          DECIMAL(10,0),
  producttype                    FOR COLUMN prodtyp         CHAR(1) NOT NULL DEFAULT 'I',
  imageurl                                                  VARCHAR(120),
  descriptionurl                 FOR COLUMN descurl         VARCHAR(120),
