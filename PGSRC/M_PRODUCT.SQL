  CONSTRAINT m_product_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT m_product_isbom_check CHECK (isbom in ('Y','N')),
  CONSTRAINT m_product_isinvoiceprintdetails_check CHECK (isinvoiceprintdetails in ('Y','N')),
  CONSTRAINT m_product_iskanban_check CHECK (iskanban in ('Y','N')),
  CONSTRAINT m_product_ismanufactured_check CHECK (ismanufactured in ('Y','N')),
  CONSTRAINT m_product_isphantom_check CHECK (isphantom in ('Y','N')),
  CONSTRAINT m_product_ispicklistprintdetails_check CHECK (ispicklistprintdetails in ('Y','N')),
  CONSTRAINT m_product_ispurchased_check CHECK (ispurchased in ('Y','N')),
  CONSTRAINT m_product_issold_check CHECK (issold in ('Y','N')),
  CONSTRAINT m_product_isstocked_check CHECK (isstocked in ('Y','N')),
  CONSTRAINT m_product_istoformule_check CHECK (istoformule in ('Y','N')),
  CONSTRAINT m_product_isverified_check CHECK (isverified in ('Y','N'))
