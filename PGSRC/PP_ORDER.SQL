  CONSTRAINT pp_order_isprinted_check CHECK (isprinted in ('Y','N')),
  CONSTRAINT pp_order_isqtypercentage_check CHECK (isqtypercentage in ('Y','N')),
  CONSTRAINT pp_order_isselected_check CHECK (isselected in ('Y','N')),
  CONSTRAINT pp_order_issotrx_check CHECK (issotrx in ('Y','N')),
  CONSTRAINT pp_order_processed_check CHECK (processed in ('Y','N'))
)
