  isactive                                                  CHAR(1) NOT NULL,
  isapproved                                                CHAR(1) NOT NULL DEFAULT 'N',
  isprinted                                                 CHAR(1) NOT NULL DEFAULT 'N',
  isqtypercentage                FOR COLUMN isqtypc         CHAR(1),
  issotrx                                                   CHAR(1) NOT NULL DEFAULT 'N',
  isselected                                                CHAR(1) NOT NULL DEFAULT 'N',
  line                                                      DECIMAL(10,0) NOT NULL,
  lot                                                       VARCHAR(20),
  m_attributesetinstance_id      FOR COLUMN m_asi           DECIMAL(10,0),
  ordertype                                                 VARCHAR(1),
  pp_order_id                    FOR COLUMN pp_ordid        DECIMAL(10,0) NOT NULL,
  pp_product_bom_id              FOR COLUMN pp_pbomid       DECIMAL(10,0) NOT NULL,
