-- Table: i_product

-- DROP TABLE i_product;

CREATE TABLE i_product
(
  i_product_id                   FOR COLUMN i_pid           DECIMAL(10,0) NOT NULL,
  ad_client_id                   FOR COLUMN ad_cid          DECIMAL(10,0),
  ad_org_id                                                 DECIMAL(10,0),
  isactive                                                  CHAR(1) DEFAULT 'Y',
  created                                                   TIMESTAMP WITH DEFAULT CURRENT_TIMESTAMP,
  createdby                                                 DECIMAL(10,0),
  updated                                                   TIMESTAMP WITH DEFAULT CURRENT_TIMESTAMP,
  updatedby                                                 DECIMAL(10,0),
  i_isimported                   FOR COLUMN i_ii            CHAR(1) NOT NULL DEFAULT 'N',
  i_errormsg                                                VARCHAR(2000),
  m_product_id                   FOR COLUMN m_pid           DECIMAL(10,0),
  value                                                     VARCHAR(40),
  name                                                      VARCHAR(255),
  description                    FOR COLUMN desc            VARCHAR(255),
  documentnote                   FOR COLUMN docnote         VARCHAR(2000),
  help                                                      VARCHAR(2000),
  upc                                                       VARCHAR(30),
  sku                                                       VARCHAR(30),
  x12de355                                                  VARCHAR(4),
  c_uom_id                                                  DECIMAL(10,0),
  productcategory_value          FOR COLUMN prdcatval       VARCHAR(40),
  m_product_category_id          FOR COLUMN m_pcid          DECIMAL(10,0),
  producttype                    FOR COLUMN prodtyp         CHAR(1) DEFAULT 'I',
  classification                 FOR COLUMN clsfn           VARCHAR(12),
  volume                                                    DECFLOAT(16) DEFAULT 0,
  weight                                                    DECFLOAT(16) DEFAULT 0,
  shelfwidth                                                DECIMAL(10,0),
  shelfheight                    FOR COLUMN slfhgt          DECIMAL(10,0),
  shelfdepth                                                DECIMAL(10,0),
  unitsperpallet                 FOR COLUMN uppal           DECIMAL(10,0),
  discontinued                   FOR COLUMN discont         CHAR(1) DEFAULT 'N',
  discontinuedby                 FOR COLUMN dscby           TIMESTAMP,
  imageurl                                                  VARCHAR(120),
  descriptionurl                 FOR COLUMN descurl         VARCHAR(120),
  bpartner_value                 FOR COLUMN bpval           VARCHAR(40),
  c_bpartner_id                  FOR COLUMN c_bpid          DECIMAL(10,0),
  iso_code                                                  character(3),
  c_currency_id                  FOR COLUMN c_cuid          DECIMAL(10,0),
  pricelist                                                 DECFLOAT(16) DEFAULT 0,
  pricepo                                                   DECFLOAT(16) DEFAULT 0,
  royaltyamt                                                DECFLOAT(16) DEFAULT 0,
  priceeffective                 FOR COLUMN prceft          TIMESTAMP,
  vendorproductno                FOR COLUMN vprodnum        VARCHAR(40),
  vendorcategory                 FOR COLUMN vndcat          VARCHAR(30),
  manufacturer                   FOR COLUMN manuf           VARCHAR(30),
  order_min                                                 DECFLOAT(16) DEFAULT 0,
  order_pack                                                DECFLOAT(16) DEFAULT 0,
  costperorder                   FOR COLUMN cstpord         DECFLOAT(16) DEFAULT 0,
  deliverytime_promised          FOR COLUMN dlytp           DECIMAL(10,0),
  processing                                                CHAR(1),
  processed                                                 CHAR(1) DEFAULT 'N',
  pricestd                                                  DECFLOAT(16),
  pricelimit                                                DECFLOAT(16),
  discontinuedat                 FOR COLUMN discontdte      TIMESTAMP,
  m_product_classification_id    FOR COLUMN m_prdcfnid      DECIMAL(10,0),
  m_product_class_id             FOR COLUMN m_prdclsid      DECIMAL(10,0),
  m_product_group_id             FOR COLUMN m_pgrpid        DECIMAL(10,0),
  productclass_value             FOR COLUMN prclsval        VARCHAR(40),
  productclassification_value    FOR COLUMN prcfnval        VARCHAR(40),
  productgroup_value             FOR COLUMN prgrpval        VARCHAR(40),
  uuid                                                      VARCHAR(36) DEFAULT NULL,
  CONSTRAINT i_product_pkey PRIMARY KEY (i_product_id),
  CONSTRAINT i_product_discontinued_check CHECK (discontinued in ('Y','N')),
  CONSTRAINT i_product_isactive_check CHECK (isactive in ('Y','N')),
  CONSTRAINT i_product_processed_check CHECK (processed in ('Y','N'))
)
;

